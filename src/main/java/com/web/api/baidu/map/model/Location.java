package com.web.api.baidu.map.model;

/**
 * 经纬度
 */
public class Location {
	private float lng;
	private float lat;

	public float getLng() {
		return lng;
	}

	public void setLng(float lng) {
		this.lng = lng;
	}

	public float getLat() {
		return lat;
	}

	public void setLat(float lat) {
		this.lat = lat;
	}

}
